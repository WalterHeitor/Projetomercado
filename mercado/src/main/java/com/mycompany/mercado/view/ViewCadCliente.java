     public void editar() {
        EntityManager manager = Persistence.createEntityManagerFactory("vendas").
                createEntityManager();
        try {
            manager.getTransaction().begin();
            cliente = ClienteDAO.getInstance().getById(Integer.parseInt(codigoC.getText()));
            cliente.setNome(nomeC.getText().trim());
            cliente.setCpf(cpfC.getText().trim());
            cliente.setEmail(emailC.getText().trim());
            cliente.setDataCadastro((GregorianCalendar) dateCadCliente.getCalendar());

            endereco = EnderecoDAO.getInstance().getById(cliente.getEnderecos().get(cliente.getEnderecos().size() - 1).getId());
            endereco.setLogadouro(enderecoC.getText().trim());
            endereco.setComplemeto(complementoC.getText().trim());
            endereco.setBairro(bairroC.getText().trim());
            endereco.setCep(cepC.getText().trim());
            endereco.setPessoa(cliente);

            cidade = CidadeDAO.getInstance().getById(endereco.getCidade().getId());
            
            estado = EstadoDAO.getInstance().getNome(estadoC.getSelectedItem().toString());

            estado.setNome(estadoC.getSelectedItem().toString());

            cidade.setEstado(estado);

            endereco.setCidade(cidade);

            manager.merge(cliente);
            manager.merge(endereco);
           // manager.merge(estado);
            manager.merge(cidade);
            manager.getTransaction().commit();
            JOptionPane.showMessageDialog(null, "Cadastro do cliente editado com sucesso");
        } catch (Exception e) {
            System.out.println("Erro ao Editar: " + e);
            JOptionPane.showMessageDialog(null, e.getMessage(), "Cadastro nao editado",
                    JOptionPane.ERROR_MESSAGE);
            manager.getTransaction().rollback();
        } finally {
            manager.close();
        }
        carregarClientes();
        

    }

