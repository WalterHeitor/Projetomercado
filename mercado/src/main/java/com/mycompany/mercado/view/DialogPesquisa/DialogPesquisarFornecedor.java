    public void carregarjTableFornecedor() {
        List<Fornecedor> fornecedores = FornecedorDAO.getInstance().findAll();
        DefaultTableModel model = (DefaultTableModel) jTableFornecedor.getModel();
        model.setNumRows(0);
        //Inserir Fornecedores
        for (Fornecedor f : fornecedores) {
            model.addRow(new Object[]{
                f.getId_pessoa(),
                f.getNome(),
                f.getRazaoSociao(),
                f.getCnpj(),
                f.getEmail(),
                f.getFantasia()
            });
        }
    }
    public void alterarTable(){
        if(jTableFornecedor.getSelectedRow() != -1){
            Integer codigo = (Integer) jTableFornecedor.getValueAt(jTableFornecedor.getSelectedRow(), 0);
            fornecedor = FornecedorDAO.getInstance().getById(codigo);
            forncSelecionado.setText(fornecedor.toString());
            dialogCadProduto = DialogCadProduto.getInstance();
            dialogCadProduto.retornaPesquisa(fornecedor);
            
            dialogCadProduto.setVisible(true);
        }
    }
    public void novoFornecedor(){
        ViewMenu.getInstance().dialogCadFornecedor();
    }

